#!/bin/usr/env python3

x = 5
print(x)

x = 'String'
print(x)

# Boolean
x = True
y = False

# Integer
a = 10
b = 0b10 # 2
c = 0o10
d = 0x10

print(a)
print(b)
print(c)
print(d)

# Float
a = 10.1
b = 5e10

print(a)
print(b)

# Complex 

comp = 1+5j
comp2 = 2+6j

print(comp + comp2)

# String

c = 'I am a string'
c = "I am a string"

print(c)

# Escape Characters
# \n - New line
# \t - tab
# \\

c = "I am \n a new line"

print(c)

# Raw String

c = r'I am \n a new line'

print(c)

# Triple Quoted Strings

c = '''This is my
string that has  
spaces and new lines'''

print(c)

